# working with basic math opertors

x = 10
y = 5

add = x + y
subtract = x - y
multiply = x * y
division = x // y
modulus = x % y

print(f'{x} + {y} = {add}')
print(f'{x} - {y} = {subtract}')
print(f'{x} x {y} = {multiply}')
print(f'{x} / {y} = {division}')
print(f'{x} % {y} = {modulus} - therfore: {x} is perfectly divisible by {y}')
